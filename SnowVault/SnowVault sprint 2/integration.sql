CREATE OR REPLACE VIEW ANALYTICAL.VW_PATIENT_360 AS
SELECT 
    dp.PATIENT_ID,
    dp.NAME,
    dp.GENDER,
    dp.CITY,

    COALESCE(fa.TOTAL_APPOINTMENTS, 0) AS TOTAL_APPOINTMENTS,
    COALESCE(fmr.TOTAL_RECORDS, 0) AS TOTAL_RECORDS,
    COALESCE(fb.TOTAL_BILL, 0) AS TOTAL_BILL,
    COALESCE(fb.TOTAL_PAID, 0) AS TOTAL_PAID

FROM CURATED.DIM_PATIENT dp

-- ✅ APPOINTMENTS (DEDUP + SAFE)
LEFT JOIN (
    SELECT 
        PATIENT_KEY,
        COUNT(DISTINCT APPOINTMENT_ID) AS TOTAL_APPOINTMENTS
    FROM CURATED.FACT_APPOINTMENTS
    WHERE IS_VALID_RECORD = TRUE
      AND PATIENT_KEY IS NOT NULL
    GROUP BY PATIENT_KEY
) fa 
ON dp.PATIENT_KEY = fa.PATIENT_KEY

-- ✅ MEDICAL RECORDS
LEFT JOIN (
    SELECT 
        PATIENT_KEY,
        COUNT(DISTINCT RECORD_ID) AS TOTAL_RECORDS
    FROM CURATED.FACT_MEDICAL_RECORDS
    WHERE IS_VALID_RECORD = TRUE
      AND PATIENT_KEY IS NOT NULL
    GROUP BY PATIENT_KEY
) fmr 
ON dp.PATIENT_KEY = fmr.PATIENT_KEY

-- ✅ BILLING (FIX NEGATIVE + NULL)
LEFT JOIN (
    SELECT 
        PATIENT_KEY,
        SUM(
            CASE 
                WHEN TOTAL_AMOUNT IS NULL OR TOTAL_AMOUNT < 0 THEN 0 
                ELSE TOTAL_AMOUNT 
            END
        ) AS TOTAL_BILL,

        SUM(
Selection deleted

            CASE 
                WHEN PAID_AMOUNT IS NULL OR PAID_AMOUNT < 0 THEN 0 
                ELSE PAID_AMOUNT 
            END
        ) AS TOTAL_PAID

    FROM CURATED.FACT_BILLING
    WHERE IS_VALID_RECORD = TRUE
      AND PATIENT_KEY IS NOT NULL
    GROUP BY PATIENT_KEY
) fb 
ON dp.PATIENT_KEY = fb.PATIENT_KEY

WHERE dp.IS_CURRENT = 'Y';

