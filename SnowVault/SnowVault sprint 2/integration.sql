USE SCHEMA CURATED;

-- Clear existing data
TRUNCATE TABLE DIM_PATIENT;

-- MERGE for DIM_PATIENT with SCD Type 2
MERGE INTO DIM_PATIENT AS tgt
USING (
    SELECT 
        PATIENT_ID,
        NAME,
        DOB,
        GENDER,
        EMAIL,
        PHONE,
        CITY,
        STATE,
        CURRENT_DATE() AS LOAD_DATE,
        ROW_NUMBER() OVER (PARTITION BY PATIENT_ID ORDER BY CREATED_ON DESC) AS RN
    FROM VALIDATED.VALIDATED_PATIENTS
    WHERE IS_VALID_RECORD = TRUE
    QUALIFY RN = 1
) AS src
ON tgt.PATIENT_ID = src.PATIENT_ID AND tgt.IS_CURRENT = 'Y'

-- When matched and data changed, close the old record
WHEN MATCHED AND (
    tgt.NAME != src.NAME OR
    tgt.CITY != src.CITY OR
    tgt.STATE != src.STATE OR
    tgt.PHONE != src.PHONE OR
    tgt.EMAIL != src.EMAIL
) THEN UPDATE SET
    tgt.IS_CURRENT = 'N',
    tgt.VALID_TO = src.LOAD_DATE

-- Insert new record (for new patients or changed records)
WHEN NOT MATCHED THEN INSERT (
    PATIENT_ID, NAME, DOB, GENDER, EMAIL, PHONE, CITY, STATE,
    VALID_FROM, VALID_TO, IS_CURRENT, VERSION_NUMBER,
    CREATED_BY, CREATED_ON, SOURCE_TABLE
) VALUES (
    src.PATIENT_ID, src.NAME, src.DOB, src.GENDER, src.EMAIL, src.PHONE,
    src.CITY, src.STATE,
    src.LOAD_DATE, TO_DATE('9999-12-31'), 'Y',
    COALESCE((SELECT MAX(VERSION_NUMBER) + 1 FROM DIM_PATIENT WHERE PATIENT_ID = src.PATIENT_ID), 1),
    CURRENT_USER(), CURRENT_TIMESTAMP(), 'VALIDATED_PATIENTS'
);


USE SCHEMA CURATED;

-- Clear existing data
TRUNCATE TABLE DIM_DOCTOR;

-- MERGE for DIM_DOCTOR with SCD Type 2
MERGE INTO DIM_DOCTOR AS tgt
USING (
    SELECT DISTINCT
        DOCTOR_ID,
        DOCTOR_NAME,
        SPECIALIZATION,
        DEPARTMENT,
        CURRENT_DATE() AS LOAD_DATE
    FROM (
        SELECT 
            DOCTOR AS DOCTOR_ID,
            DOCTOR AS DOCTOR_NAME,
            DEPARTMENT AS SPECIALIZATION,
            DEPARTMENT,
            ROW_NUMBER() OVER (PARTITION BY DOCTOR ORDER BY CREATED_ON DESC) AS RN
        FROM VALIDATED.VALIDATED_APPOINTMENTS
        WHERE IS_VALID_RECORD = TRUE
    )
    WHERE RN = 1
) AS src
ON tgt.DOCTOR_ID = src.DOCTOR_ID AND tgt.IS_CURRENT = 'Y'

-- When matched and data changed, close the old record
WHEN MATCHED AND (
    tgt.DOCTOR_NAME != src.DOCTOR_NAME OR
    tgt.SPECIALIZATION != src.SPECIALIZATION OR
    tgt.DEPARTMENT != src.DEPARTMENT
) THEN UPDATE SET
    tgt.IS_CURRENT = 'N',
    tgt.VALID_TO = src.LOAD_DATE

-- Insert new record
WHEN NOT MATCHED THEN INSERT (
    DOCTOR_ID, DOCTOR_NAME, SPECIALIZATION, DEPARTMENT,
    VALID_FROM, VALID_TO, IS_CURRENT, VERSION_NUMBER,
    CREATED_BY, CREATED_ON, SOURCE_TABLE
) VALUES (
    src.DOCTOR_ID, src.DOCTOR_NAME, src.SPECIALIZATION, src.DEPARTMENT,
    src.LOAD_DATE, TO_DATE('9999-12-31'), 'Y',
    COALESCE((SELECT MAX(VERSION_NUMBER) + 1 FROM DIM_DOCTOR WHERE DOCTOR_ID = src.DOCTOR_ID), 1),
