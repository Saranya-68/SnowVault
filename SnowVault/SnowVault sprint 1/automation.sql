USE DATABASE SNOW_VALUT;
USE SCHEMA CURATED;

CREATE OR REPLACE PROCEDURE SP_LOAD_CURATED()
RETURNS STRING
LANGUAGE SQL
AS
$$
BEGIN

-- =============================================
-- DIM_PATIENT (SCD TYPE 2)
-- =============================================

MERGE INTO DIM_PATIENT tgt
USING (
    SELECT *
    FROM VALIDATED.ST_PATIENTS
    WHERE METADATA$ACTION IN ('INSERT','UPDATE')
) src
ON tgt.PATIENT_ID = src.PATIENT_ID AND tgt.IS_CURRENT = 'Y'

WHEN MATCHED AND (
    tgt.NAME != src.NAME OR
    tgt.CITY != src.CITY OR
    tgt.STATE != src.STATE OR
    tgt.PHONE != src.PHONE OR
    tgt.EMAIL != src.EMAIL
)
THEN UPDATE SET
    tgt.IS_CURRENT = 'N',
    tgt.VALID_TO = CURRENT_DATE()

WHEN NOT MATCHED THEN INSERT (
    PATIENT_ID, NAME, DOB, GENDER, EMAIL, PHONE, CITY, STATE,
    VALID_FROM, VALID_TO, IS_CURRENT, VERSION_NUMBER,
    CREATED_BY, CREATED_ON, SOURCE_TABLE
)
VALUES (
    src.PATIENT_ID, src.NAME, src.DOB, src.GENDER, src.EMAIL, src.PHONE,
    src.CITY, src.STATE,
    CURRENT_DATE(), TO_DATE('9999-12-31'), 'Y',
    1,
    CURRENT_USER(), CURRENT_TIMESTAMP(), 'VALIDATED_PATIENTS'
);

-- =============================================
-- DIM_DOCTOR (SCD TYPE 2)
-- =============================================

MERGE INTO DIM_DOCTOR tgt
USING (
    SELECT DISTINCT
        DOCTOR AS DOCTOR_ID,
        DOCTOR AS DOCTOR_NAME,
        DEPARTMENT AS SPECIALIZATION,
        DEPARTMENT
    FROM VALIDATED.ST_APPOINTMENTS
    WHERE METADATA$ACTION IN ('INSERT','UPDATE')
) src
ON tgt.DOCTOR_ID = src.DOCTOR_ID AND tgt.IS_CURRENT = 'Y'

WHEN MATCHED AND (
    tgt.DOCTOR_NAME != src.DOCTOR_NAME OR
    tgt.SPECIALIZATION != src.SPECIALIZATION OR
    tgt.DEPARTMENT != src.DEPARTMENT
)
THEN UPDATE SET
    tgt.IS_CURRENT = 'N',
    tgt.VALID_TO = CURRENT_DATE()

WHEN NOT MATCHED THEN INSERT (
    DOCTOR_ID, DOCTOR_NAME, SPECIALIZATION, DEPARTMENT,
    VALID_FROM, VALID_TO, IS_CURRENT, VERSION_NUMBER,
    CREATED_BY, CREATED_ON, SOURCE_TABLE
)
VALUES (
    src.DOCTOR_ID, src.DOCTOR_NAME, src.SPECIALIZATION, src.DEPARTMENT,
    CURRENT_DATE(), TO_DATE('9999-12-31'), 'Y',
    1,
    CURRENT_USER(), CURRENT_TIMESTAMP(), 'VALIDATED_APPOINTMENTS'
);

-- =============================================
-- DIM_DEPARTMENT
-- =============================================

MERGE INTO DIM_DEPARTMENT tgt
USING (
    SELECT DISTINCT
        UPPER(SUBSTR(DEPARTMENT,1,4)) AS DEPT_CODE,
        INITCAP(DEPARTMENT) AS DEPT_NAME
    FROM VALIDATED.ST_APPOINTMENTS
    WHERE METADATA$ACTION IN ('INSERT','UPDATE')
) src
ON tgt.DEPT_CODE = src.DEPT_CODE

WHEN MATCHED THEN UPDATE SET
    tgt.DEPT_NAME = src.DEPT_NAME

WHEN NOT MATCHED THEN INSERT (
    DEPT_CODE, DEPT_NAME, CREATED_BY, CREATED_ON
)
VALUES (
    src.DEPT_CODE, src.DEPT_NAME, CURRENT_USER(), CURRENT_TIMESTAMP()
);

-- =============================================
-- FACT_APPOINTMENTS
-- =============================================

MERGE INTO FACT_APPOINTMENTS tgt
USING (
    SELECT 
        A.APPOINTMENT_ID,
        dp.PATIENT_KEY,
        dd.DOCTOR_KEY,
        dpt.DEPT_KEY,
        A.APPOINTMENT_DATE,
        A.STATUS,
        A.IS_VALID_RECORD
    FROM VALIDATED.ST_APPOINTMENTS A
    LEFT JOIN DIM_PATIENT dp 
        ON A.PATIENT_ID = dp.PATIENT_ID AND dp.IS_CURRENT = 'Y'
    LEFT JOIN DIM_DOCTOR dd 
        ON A.DOCTOR = dd.DOCTOR_ID AND dd.IS_CURRENT = 'Y'
    LEFT JOIN DIM_DEPARTMENT dpt 
        ON UPPER(SUBSTR(A.DEPARTMENT,1,4)) = dpt.DEPT_CODE
    WHERE METADATA$ACTION IN ('INSERT','UPDATE')
) src
ON tgt.APPOINTMENT_ID = src.APPOINTMENT_ID

WHEN MATCHED THEN UPDATE SET
    tgt.PATIENT_KEY = src.PATIENT_KEY,
    tgt.DOCTOR_KEY = src.DOCTOR_KEY,
    tgt.DEPT_KEY = src.DEPT_KEY,
    tgt.APPOINTMENT_DATE = src.APPOINTMENT_DATE,
    tgt.STATUS = src.STATUS,
    tgt.IS_VALID_RECORD = src.IS_VALID_RECORD

WHEN NOT MATCHED THEN INSERT (
    APPOINTMENT_ID, PATIENT_KEY, DOCTOR_KEY, DEPT_KEY,
    APPOINTMENT_DATE, STATUS, IS_VALID_RECORD,
    CREATED_BY, CREATED_ON
)
VALUES (
    src.APPOINTMENT_ID, src.PATIENT_KEY, src.DOCTOR_KEY, src.DEPT_KEY,
    src.APPOINTMENT_DATE, src.STATUS, src.IS_VALID_RECORD,
    CURRENT_USER(), CURRENT_TIMESTAMP()
);

-- =============================================
-- FACT_MEDICAL_RECORDS
-- =============================================

MERGE INTO FACT_MEDICAL_RECORDS tgt
USING (
    SELECT 
        M.RECORD_ID,
        dp.PATIENT_KEY,
        M.RECORD_DATE,
        M.DIAGNOSIS,
        M.PROCEDURE AS PROCEDURE_NAME,
        M.IS_VALID_RECORD
    FROM VALIDATED.ST_MEDICAL_RECORDS M
    LEFT JOIN DIM_PATIENT dp 
        ON M.PATIENT_ID = dp.PATIENT_ID AND dp.IS_CURRENT = 'Y'
    WHERE METADATA$ACTION IN ('INSERT','UPDATE')
) src
ON tgt.RECORD_ID = src.RECORD_ID

WHEN MATCHED THEN UPDATE SET
    tgt.PATIENT_KEY = src.PATIENT_KEY,
    tgt.RECORD_DATE = src.RECORD_DATE,
    tgt.DIAGNOSIS = src.DIAGNOSIS,
    tgt.PROCEDURE_NAME = src.PROCEDURE_NAME,
    tgt.IS_VALID_RECORD = src.IS_VALID_RECORD

WHEN NOT MATCHED THEN INSERT (
    RECORD_ID, PATIENT_KEY, RECORD_DATE,
    DIAGNOSIS, PROCEDURE_NAME, IS_VALID_RECORD,
    CREATED_BY, CREATED_ON
)
VALUES (
    src.RECORD_ID, src.PATIENT_KEY, src.RECORD_DATE,
    src.DIAGNOSIS, src.PROCEDURE_NAME, src.IS_VALID_RECORD,
    CURRENT_USER(), CURRENT_TIMESTAMP()
);

-- =============================================
-- FACT_BILLING
-- =============================================

MERGE INTO FACT_BILLING tgt
USING (
    SELECT 
        B.BILL_ID,
        dp.PATIENT_KEY,
        fa.APPOINTMENT_KEY,
        B.BILL_DATE,
        B.TOTAL_AMOUNT,
        B.PAID_AMOUNT,
        B.PAYMENT_STATUS,
        B.IS_VALID_RECORD
    FROM VALIDATED.ST_BILLING B
    LEFT JOIN DIM_PATIENT dp 
        ON B.PATIENT_ID = dp.PATIENT_ID AND dp.IS_CURRENT = 'Y'
    LEFT JOIN FACT_APPOINTMENTS fa 
        ON B.APPOINTMENT_ID = fa.APPOINTMENT_ID
    WHERE METADATA$ACTION IN ('INSERT','UPDATE')
) src
ON tgt.BILL_ID = src.BILL_ID

WHEN MATCHED THEN UPDATE SET
    tgt.PATIENT_KEY = src.PATIENT_KEY,
    tgt.APPOINTMENT_KEY = src.APPOINTMENT_KEY,
    tgt.BILL_DATE = src.BILL_DATE,
    tgt.TOTAL_AMOUNT = src.TOTAL_AMOUNT,
    tgt.PAID_AMOUNT = src.PAID_AMOUNT,
    tgt.PAYMENT_STATUS = src.PAYMENT_STATUS,
    tgt.IS_VALID_RECORD = src.IS_VALID_RECORD

WHEN NOT MATCHED THEN INSERT (
    BILL_ID, PATIENT_KEY, APPOINTMENT_KEY,
    BILL_DATE, TOTAL_AMOUNT, PAID_AMOUNT,
    PAYMENT_STATUS, IS_VALID_RECORD,
    CREATED_BY, CREATED_ON
)
VALUES (
    src.BILL_ID, src.PATIENT_KEY, src.APPOINTMENT_KEY,
    src.BILL_DATE, src.TOTAL_AMOUNT, src.PAID_AMOUNT,
    src.PAYMENT_STATUS, src.IS_VALID_RECORD,
    CURRENT_USER(), CURRENT_TIMESTAMP()
);

RETURN 'SUCCESS';

END;
$$;



