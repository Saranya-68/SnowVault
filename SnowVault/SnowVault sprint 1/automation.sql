CREATE OR REPLACE TASK TASK_VALIDATE_PATIENTS
WAREHOUSE = COMPUTE_WH
SCHEDULE = '10 seconds'
WHEN SYSTEM$STREAM_HAS_DATA('RAW.STRM_RAW_PATIENTS')
AS

MERGE INTO VALIDATED.VALIDATED_PATIENTS AS T
USING
(
    SELECT
        TRIM(PATIENT_ID) AS PATIENT_ID,
        INITCAP(TRIM(NAME)) AS NAME,
        TRY_TO_DATE(DOB) AS DOB,
        UPPER(TRIM(GENDER)) AS GENDER,
        LOWER(TRIM(EMAIL)) AS EMAIL,
        REGEXP_REPLACE(PHONE,'[^0-9]','') AS PHONE,
        INITCAP(TRIM(CITY)) AS CITY,
        UPPER(TRIM(STATE)) AS STATE,
        TRUE AS IS_VALID_RECORD,
        'AUTO_LOAD' AS VALIDATION_ERRORS,
        CURRENT_USER() AS CREATED_BY,
        CURRENT_TIMESTAMP() AS CREATED_ON
    FROM RAW.STRM_RAW_PATIENTS
) S
ON T.PATIENT_ID = S.PATIENT_ID

WHEN MATCHED THEN UPDATE SET
T.NAME = S.NAME,
T.DOB = S.DOB,
T.GENDER = S.GENDER,
T.EMAIL = S.EMAIL,
T.PHONE = S.PHONE,
T.CITY = S.CITY,
T.STATE = S.STATE,
T.IS_VALID_RECORD = TRUE,
T.VALIDATION_ERRORS = 'AUTO_UPDATED',
T.CREATED_ON = CURRENT_TIMESTAMP()

WHEN NOT MATCHED THEN INSERT
(
PATIENT_ID, NAME, DOB, GENDER, EMAIL, PHONE, CITY, STATE,
IS_VALID_RECORD, VALIDATION_ERRORS, CREATED_BY, CREATED_ON
)
VALUES
(
S.PATIENT_ID, S.NAME, S.DOB, S.GENDER, S.EMAIL, S.PHONE, S.CITY, S.STATE,
S.IS_VALID_RECORD, S.VALIDATION_ERRORS, S.CREATED_BY, S.CREATED_ON
);


