USE SCHEMA CURATED;

-- Clear existing data
TRUNCATE TABLE DIM_PATIENT;

-- MERGE for DIM_PATIENT with SCD Type 2
MERGE INTO DIM_PATIENT AS tgt
USING (
    SELECT 
        PATIENT_ID,
        NAME,
        DOB,
        GENDER,
        EMAIL,
        PHONE,
        CITY,
        STATE,
        CURRENT_DATE() AS LOAD_DATE,
        ROW_NUMBER() OVER (PARTITION BY PATIENT_ID ORDER BY CREATED_ON DESC) AS RN
    FROM VALIDATED.VALIDATED_PATIENTS
    WHERE IS_VALID_RECORD = TRUE
    QUALIFY RN = 1
) AS src
ON tgt.PATIENT_ID = src.PATIENT_ID AND tgt.IS_CURRENT = 'Y'

-- When matched and data changed, close the old record
WHEN MATCHED AND (
    tgt.NAME != src.NAME OR
    tgt.CITY != src.CITY OR
    tgt.STATE != src.STATE OR
    tgt.PHONE != src.PHONE OR
    tgt.EMAIL != src.EMAIL
) THEN UPDATE SET
    tgt.IS_CURRENT = 'N',
    tgt.VALID_TO = src.LOAD_DATE

-- Insert new record (for new patients or changed records)
WHEN NOT MATCHED THEN INSERT (
    PATIENT_ID, NAME, DOB, GENDER, EMAIL, PHONE, CITY, STATE,
    VALID_FROM, VALID_TO, IS_CURRENT, VERSION_NUMBER,
    CREATED_BY, CREATED_ON, SOURCE_TABLE
) VALUES (
    src.PATIENT_ID, src.NAME, src.DOB, src.GENDER, src.EMAIL, src.PHONE,
    src.CITY, src.STATE,
    src.LOAD_DATE, TO_DATE('9999-12-31'), 'Y',
    COALESCE((SELECT MAX(VERSION_NUMBER) + 1 FROM DIM_PATIENT WHERE PATIENT_ID = src.PATIENT_ID), 1),
    CURRENT_USER(), CURRENT_TIMESTAMP(), 'VALIDATED_PATIENTS'
);


USE SCHEMA CURATED;

-- Clear existing data
TRUNCATE TABLE DIM_DOCTOR;

-- MERGE for DIM_DOCTOR with SCD Type 2
MERGE INTO DIM_DOCTOR AS tgt
USING (
    SELECT DISTINCT
        DOCTOR_ID,
        DOCTOR_NAME,
        SPECIALIZATION,
        DEPARTMENT,
        CURRENT_DATE() AS LOAD_DATE
    FROM (
        SELECT 
            DOCTOR AS DOCTOR_ID,
            DOCTOR AS DOCTOR_NAME,
            DEPARTMENT AS SPECIALIZATION,
            DEPARTMENT,
            ROW_NUMBER() OVER (PARTITION BY DOCTOR ORDER BY CREATED_ON DESC) AS RN
        FROM VALIDATED.VALIDATED_APPOINTMENTS
        WHERE IS_VALID_RECORD = TRUE
    )
    WHERE RN = 1
) AS src
ON tgt.DOCTOR_ID = src.DOCTOR_ID AND tgt.IS_CURRENT = 'Y'

-- When matched and data changed, close the old record
WHEN MATCHED AND (
    tgt.DOCTOR_NAME != src.DOCTOR_NAME OR
    tgt.SPECIALIZATION != src.SPECIALIZATION OR
    tgt.DEPARTMENT != src.DEPARTMENT
) THEN UPDATE SET
    tgt.IS_CURRENT = 'N',
    tgt.VALID_TO = src.LOAD_DATE

-- Insert new record
WHEN NOT MATCHED THEN INSERT (
    DOCTOR_ID, DOCTOR_NAME, SPECIALIZATION, DEPARTMENT,
    VALID_FROM, VALID_TO, IS_CURRENT, VERSION_NUMBER,
    CREATED_BY, CREATED_ON, SOURCE_TABLE
) VALUES (
    src.DOCTOR_ID, src.DOCTOR_NAME, src.SPECIALIZATION, src.DEPARTMENT,
    src.LOAD_DATE, TO_DATE('9999-12-31'), 'Y',
    COALESCE((SELECT MAX(VERSION_NUMBER) + 1 FROM DIM_DOCTOR WHERE DOCTOR_ID = src.DOCTOR_ID), 1),
    CURRENT_USER(), CURRENT_TIMESTAMP(), 'VALIDATED_APPOINTMENTS'
);
USE SCHEMA CURATED;

-- Clear existing data
TRUNCATE TABLE DIM_DEPARTMENT;

-- MERGE for DIM_DEPARTMENT (Only from APPOINTMENTS)
MERGE INTO DIM_DEPARTMENT AS tgt
USING (
    SELECT DISTINCT
        UPPER(SUBSTR(DEPARTMENT, 1, 4)) AS DEPT_CODE,
        INITCAP(TRIM(DEPARTMENT)) AS DEPT_NAME
    FROM VALIDATED.VALIDATED_APPOINTMENTS
    WHERE DEPARTMENT IS NOT NULL 
      AND DEPARTMENT != ''
      AND IS_VALID_RECORD = TRUE
) AS src
ON tgt.DEPT_CODE = src.DEPT_CODE

WHEN MATCHED THEN UPDATE SET
    tgt.DEPT_NAME = src.DEPT_NAME,
    tgt.CREATED_BY = CURRENT_USER(),
    tgt.CREATED_ON = CURRENT_TIMESTAMP()

WHEN NOT MATCHED THEN INSERT (
    DEPT_CODE, DEPT_NAME, CREATED_BY, CREATED_ON
) VALUES (
    src.DEPT_CODE, src.DEPT_NAME, CURRENT_USER(), CURRENT_TIMESTAMP()
);



USE SCHEMA CURATED;

-- Clear existing data
TRUNCATE TABLE FACT_APPOINTMENTS;

-- MERGE for FACT_APPOINTMENTS
MERGE INTO FACT_APPOINTMENTS AS tgt
USING (
    SELECT 
        vAPPT.APPOINTMENT_ID,
        vAPPT.PATIENT_ID,
        vAPPT.DOCTOR AS DOCTOR_ID,
        vAPPT.DEPARTMENT,
        vAPPT.APPOINTMENT_DATE,
        vAPPT.STATUS,
        vAPPT.IS_VALID_RECORD,
        
        -- Get surrogate keys from dimension tables
        dp.PATIENT_KEY,
        dd.DOCTOR_KEY,
        ddept.DEPT_KEY,
        NULL AS DATE_KEY  -- Will add later when DIM_DATE is loaded
        
    FROM VALIDATED.VALIDATED_APPOINTMENTS vAPPT
    LEFT JOIN CURATED.DIM_PATIENT dp 
        ON vAPPT.PATIENT_ID = dp.PATIENT_ID AND dp.IS_CURRENT = 'Y'
    LEFT JOIN CURATED.DIM_DOCTOR dd 
        ON vAPPT.DOCTOR = dd.DOCTOR_ID AND dd.IS_CURRENT = 'Y'
    LEFT JOIN CURATED.DIM_DEPARTMENT ddept 
        ON UPPER(SUBSTR(vAPPT.DEPARTMENT, 1, 4)) = ddept.DEPT_CODE
    WHERE vAPPT.IS_VALID_RECORD = TRUE
    
) AS src
ON tgt.APPOINTMENT_ID = src.APPOINTMENT_ID

WHEN MATCHED THEN UPDATE SET
    tgt.PATIENT_KEY = src.PATIENT_KEY,
    tgt.DOCTOR_KEY = src.DOCTOR_KEY,
    tgt.DEPT_KEY = src.DEPT_KEY,
    tgt.DATE_KEY = src.DATE_KEY,
    tgt.APPOINTMENT_DATE = src.APPOINTMENT_DATE,
    tgt.STATUS = src.STATUS,
    tgt.IS_VALID_RECORD = src.IS_VALID_RECORD,
    tgt.CREATED_BY = CURRENT_USER(),
    tgt.CREATED_ON = CURRENT_TIMESTAMP()

WHEN NOT MATCHED THEN INSERT (
    APPOINTMENT_ID,
    PATIENT_KEY,
    DOCTOR_KEY,
    DEPT_KEY,
    DATE_KEY,
    APPOINTMENT_DATE,
    STATUS,
    IS_VALID_RECORD,
    CREATED_BY,
    CREATED_ON
) VALUES (
    src.APPOINTMENT_ID,
    src.PATIENT_KEY,
    src.DOCTOR_KEY,
    src.DEPT_KEY,
    src.DATE_KEY,
    src.APPOINTMENT_DATE,
    src.STATUS,
    src.IS_VALID_RECORD,
    CURRENT_USER(),
    CURRENT_TIMESTAMP()
);

