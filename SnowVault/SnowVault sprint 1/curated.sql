USE SCHEMA CURATED;

-- =============================================
-- 1. DIM_PATIENT (SCD TYPE 2)
-- =============================================
CREATE OR REPLACE TABLE DIM_PATIENT (
    PATIENT_KEY      NUMBER AUTOINCREMENT,
    PATIENT_ID       STRING NOT NULL,
    NAME             STRING,
    DOB              DATE,
    GENDER           STRING,
    EMAIL            STRING,
    PHONE            STRING,
    CITY             STRING,
    STATE            STRING,
    
    -- SCD Type 2 Columns
    VALID_FROM       DATE DEFAULT CURRENT_DATE(),
    VALID_TO         DATE DEFAULT TO_DATE('9999-12-31', 'YYYY-MM-DD'),
    IS_CURRENT       STRING DEFAULT 'Y',
    VERSION_NUMBER   NUMBER DEFAULT 1,
    
    -- Audit
    CREATED_BY       STRING DEFAULT CURRENT_USER(),
    CREATED_ON       TIMESTAMP DEFAULT CURRENT_TIMESTAMP(),
    SOURCE_TABLE     STRING
);

-- =============================================
-- 2. DIM_DOCTOR (SCD TYPE 2)
